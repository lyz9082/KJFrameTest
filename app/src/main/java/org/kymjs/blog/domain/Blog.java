package org.kymjs.blog.domain;

import java.io.Serializable;

/**
 * 博客实体类，以后替换为KymJSBlog(先不删，留着)
 * 
 * @author kymjs (https://www.kymjs.com)
 * @since 2015-3
 */
public class Blog implements Serializable {
    private static final long serialVersionUID = 1L;

    private String id; // 个人博客列表唯一标识
    private String title; // 博客标题
    private String url; // 博客地址
    private String author; // 博主
    private String imageUrl; // 博客中的图片地址
    private String description; // 博客摘要
    private String date; // 博客发布日期 yyyy-mm-dd
    private int isRecommend; // 是否推荐 (非0即真)
    private int isAuthor; // 是否原创 (非0即真)

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public int getIsRecommend() {
        return isRecommend;
    }

    public void setIsRecommend(int isRecommend) {
        this.isRecommend = isRecommend;
    }

    public int getIsAuthor() {
        return isAuthor;
    }

    public void setIsAuthor(int isAuthor) {
        this.isAuthor = isAuthor;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

}
